                        <h1>Die Kostenübernahme trägt je nach Versicherung die Krankenkassse</h1>     
                        <p>Im privatärztlichen Bereich werden die Kosten der radiochirurgischen Behandlung nach der Gebührenordnung für Ärzte (GOÄ) berechnet und von privaten Krankenversicherungen auf dieser Basis erstattet. </p>
                        <p>Im Bereich der gesetzlichen Krankenversicherung (GKV) besteht zwischen dem Gamma Knife Zentrum Krefeld und der DAK (Deutsche Angestellten - Krankenkasse) ein Vertrag der integrierten Versorgung (IV - Vertrag), wodurch Versicherten der DAK nun als erster Gruppe von gesetzlich Versicherten in Deutschland die Gamma Knife Radiochirurgie ohne zeitaufwändiges Antragsverfahren im Bedarfsfall zur Verfügung steht. Die TK (Techniker Krankenkasse) ist diesem Vertrag ebenfalls beigetreten, so dass nun auch ihren Versicherten die Gamma Knife Radiochirurgie im Bedarfsfall zur Verfügung steht. Mit der AOK Rheinland – Hamburg, der Schwenninger BKK und der Actimonda Krankenkasse bestehen Selektivverträge, so dass die Leistung im Einzelfall nicht mehr beantragt werden muss.</p>
                        <p>Bei anderen gesetzlichen Krankenversicherungen besteht leider noch immer kein Anspruch auf Kostenerstattung im ambulanten Bereich. Obwohl die Radiochirurgie in Deutschland erfolgreich seit 1994 und weltweit seit den 1970er Jahren erbracht wird, ist eine Aufnahme der Radiochirurgie in den Leistungskatalog der GKV bis heute unterblieben. Daher müssen viele Patienten die Kostenübernahme für eine ambulante radiochirurgische Behandlung noch immer zunächst bei Ihrer Krankenversicherung beantragen. In den letzten Jahren wurde in tausenden Fällen eine Kostenübernahme im Rahmen von Einzelfallentscheidungen zugesagt.</p>
                        <p>Wir bieten Ihnen bei der Beantragung der Kostenübernahme Hilfestellungen an. Nach dem Beratungsgespräch und der Indikationsstellung erhalten Sie von uns einen Arztbrief mit einen Kostenvoranschlag. Dieses Dokument reichen Sie dann bitte selbst bei Ihrer Krankenversicherung ein. Sobald Sie von Seiten der Versicherung eine schriftliche Bestätigung erhalten haben, nehmen Sie wieder Kontakt mit uns auf, um einen Behandlungstermin zu vereinbaren. Im Falle einer Ablehnung raten wir Ihnen ebenfalls Kontakt mit uns aufzunehmen, um das weitere Vorgehen zu besprechen.</p>                       
