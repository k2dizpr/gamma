                        <p>Die große Innovation des Axesse-Linearbeschleunigers liegt in der absoluten Präzision der Strahlentherapie durch den integrierten Computertomografen. Hochauflösende Bilder machen während der Behandlung Tumoren und umliegende Organe sichtbar. Auf Grundlage der dadurch ermittelten Daten, in Kombination mit anderer bildgebenden Modalitäten, wie der hochauflösenden Kernspintomografie (MRT), die in unserer Einrichtung ebenfalls dediziert für die stereotaktische Therapie zur Verfügung steht, werden der Bestrahlungsplan und die Behandlungsposition errechnet.</p>
                        <p>Trotz einer in der Regel nicht invasiven Fixierung bringt die in alle Raumrichtungen bewegliche automatisierte Patientenauflage Patienten und Tumor mit einer Genauigkeit im Submillimeterbereich in die exakte Behandlungsposition. Auf diese Weise können Tumoren und Metastasen im Bereich des gesamten Körpers, z. B. in Prostata, Lunge, Leber und Wirbelsäule, bei entsprechender Eignung und gesicherter Indikationsstellung erfolgreich behandelt werden.</p>
                        <p>Wie auch bei der Gamma Knife-Behandlung sorgt die millimeterfeine Bündelung der Strahlen für ein hocheffektives Bestrahlungsfeld, das sich genau auf den Tumor konzentriert. Das umliegende gesunde Gewebe wird so nur minimal belastet. Die hohe Leistung des Gerätes erlaubt extrem kurze Behandlungszeiten von teilweise nur wenigen Minuten. Seine offene Bauweise und die geringe Geräuschkulisse sorgen für eine angenehme Umgebung. Die stereotaktische Behandlung mit dem Axesse-Linearbeschleuniger eignet sich als effektive und effiziente Alternative zu operativen Tumorentfernungen, wodurch invasive Eingriffe und damit verbunden lange Krankenhausaufenthalte vermieden werden.</p>
